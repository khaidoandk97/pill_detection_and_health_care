names_ = ['pill']
    
class YoloV5:
    def __init__(
        self,
        size = 640,
        conf_thres = 0.7,
        iou_thres=0.3,
        max_det=100,
        device='cpu',
        weight='yolov5s_short.pt', # weight https://github.com/ultralytics/yolov5
        names = names_,
        n_classes = len(names_)
    ):
        self.size, self.conf_thres, self.iou_thres = size, conf_thres, iou_thres
        self.max_det, self.device, self.names = max_det, device, names

        model = Model(nc=n_classes).to(device)
        model.load_state_dict(torch.load(weight, map_location=device))
        self.model = model.float().eval()

    @torch.no_grad()
    def _predict(self, img):
        # Preprocess
        im = img.copy()
        # Padded resize
        im = letterbox(im, self.size, stride=32, auto=True)[0]
        # Convert 
        im = im.transpose((2, 0, 1))[::-1] # HWC to CHW, BGR to RGB
        im = np.ascontiguousarray(im)

        im = torch.from_numpy(im).to(self.device)
        im = im.float() # unit8 to fp16/32
        im /= 255 # 0 - 255 to 0.0 - 1.0
        if len(im.shape) == 3:
            im = im[None] # expand for batch dim

        pred = self.model(im)[0]

        # NMS (Non Maximum Suppresion)
        pred = non_max_suppression(pred, self.conf_thres, self.iou_thres, None, False, max_det=self.max_det)[0]


        return pred, im

    def predict(self, img):
        pred, im = self._predict(img)
        boxes, labels, scores, names = None, None, None, None
        if len(pred):
            # Rescale boxes from image_size to im0 size
            pred[:, :4] = scale_coords(im.shape[2:], pred[:, :4], img.shape).round()  # xyxy format
            pred = pred.cpu().detach().numpy()
            boxes, labels, scores = pred[:,:4].astype(int), pred[:,5].astype(np.uint8) , pred[:,4]

            names = [self.names[label] for label in labels]
        
        return boxes, labels, scores, names 